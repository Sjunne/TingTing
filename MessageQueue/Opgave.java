package MessageQueue;

public class Opgave {
    int one;
    int two;
    int sum;

    public String toStringOne() {
        return "Opgave{" +
                "one=" + one +
                ", two=" + two +
                ", sum=" + sum +
                '}';
    }

    public int getOne() {
        return one;
    }

    public void setOne(int one) {
        this.one = one;
    }

    public int getTwo() {
        return two;
    }

    public void setTwo(int two) {
        this.two = two;
    }

    public int getSum() {
        return sum;
    }

    public void setSum(int sum) {
        this.sum = sum;
    }
}
